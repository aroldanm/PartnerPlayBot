import UIKit

extension UIColor {
    static var background: UIColor {
        return UIColor(red: 17/255, green: 17/255, blue: 17/255, alpha: 1)
    }

    static var detail: UIColor {
        return UIColor(red: 0/255, green: 122/255, blue: 255/255, alpha: 1)
    }

    static var content: UIColor {
        return UIColor.white
    }

    static var disconnected: UIColor {
        return UIColor(red: 244/255, green: 89/255, blue: 66/255, alpha: 1)
    }
}
